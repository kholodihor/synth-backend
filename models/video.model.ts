import mongoose from 'mongoose';

interface IVideo {
  title: string;
  url: string;
  user: string;
}


const VideoSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    url: {
      type: String,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model<IVideo>('Video', VideoSchema);